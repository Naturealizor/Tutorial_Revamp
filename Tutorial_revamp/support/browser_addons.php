      <h4>Firefox Installation Tips</h4>
      <p>You may grow so accustomed to some of these extensions that you'll miss your favorites in Butler labs and wish they were portable. Well, they are.</p>
      <ol>
        <li>To carry them with you, just <strong>right-click the Install Now butto</strong>n and <strong>save the .xpi file </strong>for each of your favorite extensions to a folder on your flash drive.</li>
        <li>To quickly install on another computer, open Firefox and choose <strong>Tools &gt; Addons</strong>.</li>
        <li>Drag one or several selected .xpi files from your flash drive and drop them onto the Addons window.</li>
        <li>Usually, you are required to restart Firefox to complete the installation.</li>
        <li>That's it--piece of cake.</li>
      </ol>
